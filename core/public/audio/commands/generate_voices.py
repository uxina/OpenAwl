def generate_voice(folder, filename, text):
    """生成单个语音文件"""
    output_path = BASE_DIR / folder / filename
    cmd = [
        "edge-tts",
        "--text", text,
        "--voice", VOICE,
        "--write-media", str(output_path)
    ]
    
    try:
        subprocess.run(cmd, check=True, capture_output=True)
        print(f"✓ 已生成: {folder}/{filename}")
        return True
    except subprocess.CalledProcessError as e:
        print(f"✗ 失败: {folder}/{filename} - {e}")
        return False

def main():
    """主函数"""
    print("=" * 60)
    print("阿瓦隆语音批量生成工具")
    print("=" * 60)
    
    total = sum(len(files) for files in VOICES.values())
    completed = 0
    failed = 0
    
    for folder, files in VOICES.items():
        print(f"\n【{folder}】目录:")
        for filename, text in files:
            if generate_voice(folder, filename, text):
                completed += 1
            else:
                failed += 1
    
    print("\n" + "=" * 60)
    print(f"生成完成！总计: {total}, 成功: {completed}, 失败: {failed}")
    print("=" * 60)

if __name__ == "__main__":
    main()
